
public class ProcessaCalculos {

	public void mult(double n1, double n2) {
		System.out.println("A multiplicação do primerio numero com o segundo número é: "+n1*n2);
	}
	
	public void soma(double n1, double n2) {
		System.out.println("A soma do primerio numero com o segundo número é: "+(n1+n2));
		
	}
	
	public void sub(double n1, double n2) {
		System.out.println("A subtração do primerio numero com o segundo número é: "+(n1-n2));
	}
	
	public void div(double n1, double n2) {
		System.out.println("A divisão do primerio numero com o segundo número é: "+(n1/n2));
	}
	
	
}
